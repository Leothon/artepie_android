package com.leothon.cogito.Bean;

public class CustomEnterprise {
    private String enterpriseName;
    private String enterpriseAddress;
    private String enterpriseIndustry;
    private String enterpriseWork;
    private String enterpriseSize;

    public String getEnterpriseAddress() {
        return enterpriseAddress;
    }

    public void setEnterpriseAddress(String enterpriseAddress) {
        this.enterpriseAddress = enterpriseAddress;
    }

    public String getEnterpriseIndustry() {
        return enterpriseIndustry;
    }

    public void setEnterpriseIndustry(String enterpriseIndustry) {
        this.enterpriseIndustry = enterpriseIndustry;
    }

    public String getEnterpriseName() {
        return enterpriseName;
    }

    public void setEnterpriseName(String enterpriseName) {
        this.enterpriseName = enterpriseName;
    }

    public String getEnterpriseSize() {
        return enterpriseSize;
    }

    public void setEnterpriseSize(String enterpriseSize) {
        this.enterpriseSize = enterpriseSize;
    }

    public String getEnterpriseWork() {
        return enterpriseWork;
    }

    public void setEnterpriseWork(String enterpriseWork) {
        this.enterpriseWork = enterpriseWork;
    }

}
